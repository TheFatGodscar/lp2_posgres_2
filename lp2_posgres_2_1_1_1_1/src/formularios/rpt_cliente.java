    private void btn_imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imprimirActionPerformed
        if (opcion == 1) {
            sql = "SELECT a.cod_clie,CONCAT(a.cli_nombre,' ',a.cli_apellido)AS nombre,a.cli_ci,"
            + " a.cli_direccion,a.cli_celular, b.tipo_descri "
            + " FROM clientes a, tipo_cliente b "
            + " WHERE a.cod_tipo=b.cod_tipo";
            this.generar_reporte(sql);
            this.buttonGroup1.clearSelection();
        }
        if (opcion == 2) {
            sql = "SELECT a.cod_clie,CONCAT(a.cli_nombre,' ',a.cli_apellido)AS nombre,a.cli_ci, "
            + " a.cli_direccion,a.cli_celular, b.tipo_descri "
            + " FROM clientes a, tipo_cliente b "
            + " WHERE a.cod_tipo=b.cod_tipo and "
            + "a.cod_clie >=" + desde.getText() + " AND a.cod_clie <=" + hasta.getText();
            this.generar_reporte(sql);
            this.buttonGroup1.clearSelection();
            desde.setText("");
            hasta.setText("");
        }
        if (opcion == 3) {
            sql = "SELECT a.cod_clie,CONCAT(a.cli_nombre,' ',a.cli_apellido)AS nombre,a.cli_ci, "
            + " a.cli_direccion,a.cli_celular, b.tipo_descri "
            + " FROM clientes a, tipo_cliente b "
            + " WHERE a.cod_tipo=b.cod_tipo "
            + "and b.tip_descri='" + cbo_tipo.getSelectedItem() + "'";
            this.generar_reporte(sql);
            this.buttonGroup1.clearSelection();
            cbo_tipo.removeAllItems();
        }
    }//GEN-LAST:event_btn_imprimirActionPerformed

