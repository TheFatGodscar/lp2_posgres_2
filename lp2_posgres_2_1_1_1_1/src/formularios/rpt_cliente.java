    private void cargar_combo() {
        try {
            rs1 = con.listar_datos("SELECT tip_descri FROM tipo_cliente");
            this.cbo_tipo.removeAllItems();
            while (rs1.next()) {
                this.cbo_tipo.addItem(rs1.getString("tip_descri"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(rpt_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void generar_reporte(String consulta) {
        try {
            rs = con.listar_datos(consulta);

            //envia parametros
            Map parameters = new HashMap();
            //parametros vacio
            parameters.put("", new String(""));
            //se tira null
            JasperReport jr = null;
            //se coloca la ruta de nuestro reporte
            URL url = getClass().getClassLoader().getResource("reportes/rpt_cliente.jasper");
            jr = (JasperReport) JRLoader.loadObject(url);
            //imprime en pantalla
            JasperPrint masterPrint = null;
            //conecta con la base de datos
            JRResultSetDataSource jrRS = new JRResultSetDataSource(rs);
            masterPrint = JasperFillManager.fillReport(jr, parameters, jrRS);
            //muestra el reporte
            JasperViewer ventana = new JasperViewer(masterPrint, false);
            ventana.setTitle("Vista Previa");
            ventana.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(rpt_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    

